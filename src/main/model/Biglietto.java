package main.model;

public class Biglietto {
	
	private Integer idBiglietto;
	private Integer idPassegero;
	private Integer idVolo;
	private Integer prezzo;
	
	public Integer getIdBiglietto() {
		return idBiglietto;
	}
	public void setIdBiglietto(Integer idBiglietto) {
		this.idBiglietto = idBiglietto;
	}
	public Integer getIdPassegero() {
		return idPassegero;
	}
	public void setIdPassegero(Integer idPassegero) {
		this.idPassegero = idPassegero;
	}
	public Integer getIdVolo() {
		return idVolo;
	}
	public void setIdVolo(Integer idVolo) {
		this.idVolo = idVolo;
	}
	public Integer getPrezzo() {
		return prezzo;
	}
	public void setPrezzo(Integer prezzo) {
		this.prezzo = prezzo;
	}
	
	
	
	
	
}
